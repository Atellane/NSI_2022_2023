"""définie une fonction qui calcule une expression arithmétique en fonction de la valeur des variables m, x et p"""
def partie1() :
    # m : int
    m = int(input("entrer un entier :\n"))
    # x : int
    x = int(input("entrer un 2e entier :\n"))
    # p : int
    p = int(input("entrer un 3e entier :\n"))
    fonction = m*x+p
    print(fonction)

partie1()
